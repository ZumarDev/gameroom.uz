# Standart til
DEFAULT_LANGUAGE = 'uz'


def get_translation(key, lang=None):
    """
    Berilgan kalit uchun tarjimani qaytaradi.
    
    Args:
        key: Tarjima kaliti
        lang: Til kodi (uz, ru, en). Agar berilmasa, standart til ishlatiladi.
    
    Returns:
        Tarjima qilingan matn yoki kalit (agar tarjima topilmasa)
    """
    from flask import session, g
    
    if lang is None:
        # Avval session'dan, keyin g.lang'dan, nihoyat default
        lang = session.get('language', getattr(g, 'lang', DEFAULT_LANGUAGE))
    
    if lang not in TRANSLATIONS:
        lang = DEFAULT_LANGUAGE
    
    return TRANSLATIONS[lang].get(key, key)


def get_current_language():
    """
    Joriy til kodini qaytaradi.
    
    Returns:
        Joriy til kodi (uz, ru yoki en)
    """
    from flask import session, g
    return session.get('language', getattr(g, 'lang', DEFAULT_LANGUAGE))


def get_all_translations(lang=None):
    """
    Berilgan til uchun barcha tarjimalarni qaytaradi.
    
    Args:
        lang: Til kodi. Agar berilmasa, joriy til ishlatiladi.
    
    Returns:
        Barcha tarjimalar lug'ati
    """
    from flask import session, g
    
    if lang is None:
        lang = session.get('language', getattr(g, 'lang', DEFAULT_LANGUAGE))
    
    if lang not in TRANSLATIONS:
        lang = DEFAULT_LANGUAGE
    
    return TRANSLATIONS[lang]


def get_languages():
    """
    Mavjud tillar ro'yxatini qaytaradi.
    
    Returns:
        Tillar ro'yxati (list of dicts)
    """
    return LANGUAGES


def t(key, lang=None):
    """
    get_translation uchun qisqa alias.
    """
    return get_translation(key, lang)
